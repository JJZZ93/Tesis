    private void mostrar() {        
        modelo = new DefaultTableModel();               
        ResultSet rs = Database.getTabla("select Nombre,Funcion,Sueldo_Bruto from nomina");
        modelo.setColumnIdentifiers(new Object[]{"Nombre", "Cargo", "Sueldo Bruto"});
        try {
            while (rs.next()) {
                // añade los resultado a al modelo de tabla
                modelo.addRow(new Object[]{rs.getString("Nombre"),rs.getString("Funcion"),rs.getString("Sueldo_Bruto")});
            }            
            // asigna el modelo a la tabla
            nomina.setModel(modelo);            
        } catch (Exception e) {
            System.out.println(e);
        }
    }

