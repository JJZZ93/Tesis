    private void filtro_estrategicoItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_filtro_estrategicoItemStateChanged
       String query = filtro_estrategico.getSelectedItem().toString();
       TableRowSorter<DefaultTableModel> tr= new TableRowSorter<DefaultTableModel> (modelo);
       nomina.setRowSorter(tr);
       
       if(query != " "){
           tr.setRowFilter(RowFilter.regexFilter(query));
       }else{
           nomina.setRowSorter(tr);
       }
    }//GEN-LAST:event_filtro_estrategicoItemStateChanged

